<?php


namespace App\Entity;

class Customer implements EntityInterface
{
    use EntityTrait;

    /** @var string */
    private $name;

    /**
     * @return string
     */
    public function getName(): ?string
    {
        return $this->name;
    }

    /**
     * @param string $name
     * @return Customer
     */
    public function setName(string $name): Customer
    {
        $this->name = $name;
        return $this;
    }
}
